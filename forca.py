def marca_chute_correto(chute, letras_acertadas, palavra_secreta): # O que preciso receber. Parametros para receber para a função funcionar
    index = 0  # Posição da lista, o python vai pegar a primeira letra pois marcamos o "0"
    for letra in palavra_secreta:
        if (chute == letra):
            letras_acertadas[index] = letra
        index += 1  # para conseguir pegar a proxima letra da lista


def pede_chute():
    chute = input("Qual letra? ")
    chute = chute.strip().upper()  # Strip tira espaços em branco caso o usuário escreva "   olá"
    return chute # Return = o que quero devolver pra pessoa que pergurtou


def inicializa_letras_acertadas(palavra):  # Recebendo o valor (string)
    return ["_" for letra in palavra]


